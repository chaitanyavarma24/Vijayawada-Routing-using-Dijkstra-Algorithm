#define INF INT_MAX
#define NUM_STATIONS 51

int mindistance(int distance[], bool stat[]) {
    int minimum = INF, ind = -1;
    for (int k = 0; k < NUM_STATIONS; k++) {
        if (!stat[k] && distance[k] < minimum) {
            minimum = distance[k];
            ind = k;
        }
    }
    return ind;
}

